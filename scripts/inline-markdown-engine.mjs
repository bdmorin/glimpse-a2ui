#!/usr/bin/env node
// Inlines src/markdown/a2glimpse-markdown.mjs into src/a2glimpse-host.html as
// a base64 data URL inside an <script type="importmap"> block. The block is
// placed in <head> so the import map is registered before the vendored Lit
// IIFE runs and dynamic-imports "@a2ui/markdown-it".
//
// Re-run this script whenever you edit src/markdown/a2glimpse-markdown.mjs.
// The HTML's BEGIN/END markers are required; do not remove them.

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const enginePath = resolve(root, "src/markdown/a2glimpse-markdown.mjs");
const hostPath = resolve(root, "src/a2glimpse-host.html");

const engineSrc = readFileSync(enginePath, "utf8");
const b64 = Buffer.from(engineSrc, "utf8").toString("base64");
const dataUrl = `data:text/javascript;charset=utf-8;base64,${b64}`;

const block = `<!-- BEGIN a2glimpse-markdown-engine (auto-generated by scripts/inline-markdown-engine.mjs; source: src/markdown/a2glimpse-markdown.mjs) -->
<script type="importmap">
{
  "imports": {
    "@a2ui/markdown-it": "${dataUrl}"
  }
}
</script>
<!-- END a2glimpse-markdown-engine -->`;

const html = readFileSync(hostPath, "utf8");
const beginRe = /<!-- BEGIN a2glimpse-markdown-engine[^]*?<!-- END a2glimpse-markdown-engine -->/;
let updated;
if (beginRe.test(html)) {
  updated = html.replace(beginRe, block);
} else {
  // First-time install: insert before the closing </head>.
  updated = html.replace(/<\/head>/, `${block}\n</head>`);
}
writeFileSync(hostPath, updated);
console.log(`[inline-markdown-engine] wrote ${b64.length} chars of base64 (engine ${engineSrc.length} bytes raw) into ${hostPath}`);
